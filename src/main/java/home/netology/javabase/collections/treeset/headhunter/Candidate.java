package main.java.home.netology.javabase.collections.treeset.headhunter;

public class Candidate{
    public String firstName;
    public String lastName;
    public String surName;
    public String gender;
    public byte age;
    public byte resumeRelevance;
    public byte interviewResult;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getSurName() {
        return surName;
    }

    public void setSurName(String surName) {
        this.surName = surName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public byte getAge() {
        return age;
    }

    public void setAge(byte age) {
        this.age = age;
    }

    public byte getResumeRelevance() {
        return resumeRelevance;
    }

    public void setResumeRelevance(byte resumeRelevance) {
        this.resumeRelevance = resumeRelevance;
    }

    public byte getInterviewResult() {
        return interviewResult;
    }

    public void setInterviewResult(byte interviewResult) {
        this.interviewResult = interviewResult;
    }

}
